#include <iostream>
using namespace std;

int main()
{
    int i; // i ตรงนี้ = ตัวแปร global
    int j;

    for (i = 1; i <= 5; i++) { // ใช้แบบนี้ได้เหมือนกัน แต่ควรใช้แบบนี้มากกว่า for(int i = 1; i <= 5; i++) เพราะ i ตรงนี้จะไม่เป็นตัวแปร Global
        for (j = 1; j <= 5; j++) { // ซุ่งจะทำให้ปลอดภัยมากกว่า เพราะมันถูกเปลี่ยนแปลงค่าจะภายนอกไม่ได้ มีประโยชน์เวลาเขียน code ยาวๆ
            cout<<" * ";
        }
        cout << "\n";
    }
}